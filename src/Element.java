public class Element {

    //Attributes of our Element and an object data
    private Integer key;
    private Object Data;

    public void setKey(Integer key) {
        this.key = key;
    }

    public void setData(Object data) {
        this.Data = data;
    }

    public Integer getKey() {
        return key;
    }

    public Object getData() {
        return Data;
    }

}
